    public void stockPopulate(int action, String searchKey){
        conn = DBConnection.connectDb();
        if (conn != null) {
            try {
                Vector columnNames = new Vector();
                Vector data = new Vector();
                int columns = 0;
                String p_id = "", p_name = "", stock_in = "", stock_out = "", stock = "";
                
                String sql = "";
                if (action == 0 && searchKey.equals("0")) {
                    sql = "select * from stock where quantity >0";
                }
                 else if (action == 1 && searchKey.equals("product_id")) {
                    String search = jTextField_stock_search.getText();
                    sql = "select * from stock where quantity > 0 and product_id like '%" + search + "%'";
                } else if (action == 1 && searchKey.equals("article")) {
                    String search = jTextField_stock_search.getText();
                    sql = "select * from stock where quantity > 0 and article like '%" + search + "%'";
                } 
                statement1 = conn.createStatement();
                rs = statement1.executeQuery(sql);

                columnNames.addElement("Product ID");
                columnNames.addElement("Product Name");
                columnNames.addElement("Total IN");
                columnNames.addElement("Stock");
                columnNames.addElement("Total Out");

                while (rs.next()) {

                    p_id = rs.getString("product_id");
                    p_name = rs.getString("article");
                    stock = rs.getString("quantity");
                    String total_buy = "select sum(quantity) as quantity from products where product_id = '" + p_id + "'";
                    statement_buy = conn.createStatement();
                    rs_buy = statement_buy.executeQuery(total_buy);
                    while (rs_buy.next()) {
                        stock_in = rs_buy.getString("quantity");
                    }
                    rs_buy.close();
                    statement_buy.close();
                    String total_sell = "select sum(quantity) as quantity from sell where product_id = '" + p_id + "'";
                    statement_sell = conn.createStatement();
                    rs_sell = statement_sell.executeQuery(total_sell);
                    while (rs_sell.next()) {
                        stock_out = rs_sell.getString("quantity");
                    }
                    rs_sell.close();
                    statement_sell.close();

                    Vector row = new Vector(columns);

                    row.addElement(p_id);
                    row.addElement(p_name);
                    row.addElement(stock_in);
                    row.addElement(stock);
                    row.addElement(stock_out);
                    data.addElement(row);

                }
                DefaultTableModel tableModel = new DefaultTableModel(data, columnNames);
                jTable_stock_in_out.setModel(tableModel);

            } catch (Exception e) {
                e.printStackTrace();
            } finally {
                try {
                    rs.close();
                    statement1.close();
                    conn.close();
                } catch (SQLException ex) {
                    Logger.getLogger(InventoryForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Database Connection Failed");
        }
    }
