    public int setItem(int action) throws SQLException {
        int c_db = 0;
        if (action == 0) {
            //count category and unit

            statement1 = conn.createStatement();
            String sql_c = "Select count(c_id) from categories";
            rs = statement1.executeQuery(sql_c);
            while (rs.next()) {
                c_db = rs.getInt(1);
            }
            rs.close();
            statement1.close();

        } else if (action == 1) {
            statement1 = conn.createStatement();
            String sql_u = "Select count(u_id) from unit";
            rs = statement1.executeQuery(sql_u);
            while (rs.next()) {
                c_db = rs.getInt(1);
            }
            rs.close();
            statement1.close();
        }
        return c_db;
    }

    public void addItem() {
        //get date
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        Calendar calobj = Calendar.getInstance();
        jTextField_regDate.setText(df.format(calobj.getTime()));

        SqlManager sqlManager = new SqlManager();
        try {
            String lastId = sqlManager.getLastID();
            jTextField_lastProductId.setText(lastId);
        } catch (SQLException ex) {
            Logger.getLogger(InventoryForm.class.getName()).log(Level.SEVERE, null, ex);
        }

        conn = DBConnection.connectDb();
        if (conn != null) {
            try {

                int c_db = setItem(0);
                statement1 = conn.createStatement();
                String sql = "Select c_name from categories";
                rs = statement1.executeQuery(sql);
                if (c_db != jComboBox_category.getItemCount()) {
                    while (rs.next()) {
                        jComboBox_category.addItem(rs.getString("c_name"));
                    }
                }

                rs.close();
                statement1.close();
                //add unit
                int u_db = setItem(1);
                statement1 = conn.createStatement();
                String sql1 = "Select u_name from unit";
                rs = statement1.executeQuery(sql1);
                if (u_db != jComboBox_unit.getItemCount()) {
                    while (rs.next()) {
                        jComboBox_unit.addItem(rs.getString("u_name"));
                    }
                }

                rs.close();
                statement1.close();
            } catch (SQLException ex) {
                Logger.getLogger(InventoryForm.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Database Connection Failed");
        }
    }
