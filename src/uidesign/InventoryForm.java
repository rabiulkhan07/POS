    public void loadData(int action) throws SQLException {
        conn = DBConnection.connectDb();
        if (conn != null) {
            try {
                if (action == 0) {
                    PreparedStatement preparedStatement = conn.prepareStatement("SELECT * FROM categories;");
                    ResultSet resultSetForFullTable = preparedStatement.executeQuery();
                    jTable_categoryList.setModel(DbUtils.resultSetToTableModel(resultSetForFullTable));
                    jTextField_categoryName_add.setText("");
                }
                if (action == 1) {
                    PreparedStatement preparedStatement = conn.prepareStatement("SELECT * FROM unit;");
                    ResultSet resultSetForFullTable = preparedStatement.executeQuery();
                    jTable_unitList.setModel(DbUtils.resultSetToTableModel(resultSetForFullTable));
                    jTextField_unitName_add.setText("");
                }
            } catch (Exception e) {
                e.printStackTrace();
            } finally {
                conn.close();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Can't Load data...Database Connection Failed!!!");
        }
    }
