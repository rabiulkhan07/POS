    public void dataPopulate(int action, String searchKey) {
        conn = DBConnection.connectDb();
        if (conn != null) {
            String p_id = "", artical = "", category = "", description = "", quantity = "", unit = "", b_price = "", s_price = "", discount = "",
                    total = "", paid = "", due = "", pay_type = "", reg_date = "", vat = "";
            int id = 0;
            try {
                Vector columnNames = new Vector();
                Vector data = new Vector();
                int columns = 0;
                String sql = "";
                if (action == 0 && searchKey.equals("0")) {
                    sql = "select * from products where quantity > 0";
                } else if (action == 1 && searchKey.equals("product_id")) {
                    String search = jTextField_search_param.getText();
                    sql = "select * from products where quantity > 0 and product_id like '%" + search + "%'";
                } else if (action == 1 && searchKey.equals("article")) {
                    String search = jTextField_search_param.getText();
                    sql = "select * from products where quantity > 0 and article like '%" + search + "%'";
                } else if (action == 2 && searchKey.equals("0")) {

                    String filter = jComboBox_filter.getSelectedItem().toString();
                    if (filter.equalsIgnoreCase("all")) {
                        sql = "select * from products where quantity > 0";
                    } else {
                        sql = "select * from products where quantity > 0 and category='" + filter + "'";
                    }
                }

                statement1 = conn.createStatement();
                rs = statement1.executeQuery(sql);
                columnNames.addElement("ID");
                columnNames.addElement("Product ID");
                columnNames.addElement("Article");
                columnNames.addElement("Category");
                columnNames.addElement("Description");
                columnNames.addElement("Quantity");
                columnNames.addElement("Unit");
                columnNames.addElement("Buying price");
                columnNames.addElement("Selling Price");
                columnNames.addElement("Discount %");
                columnNames.addElement("Vat %");
                columnNames.addElement("Total");
                columnNames.addElement("Paid");
                columnNames.addElement("Due");
                columnNames.addElement("Payment Type");
                columnNames.addElement("Reg. Date");

                while (rs.next()) {
                    Vector row = new Vector(columns);
                    id = rs.getInt("id");
                    p_id = rs.getString("product_id");
                    artical = rs.getString("article");
                    category = rs.getString("category");
                    description = rs.getString("description");
                    quantity = rs.getString("quantity");
                    unit = rs.getString("unit");
                    b_price = rs.getString("buying_price");
                    s_price = rs.getString("selling_price");
                    discount = rs.getString("discount");
                    vat = rs.getString("vat");
                    total = rs.getString("total");
                    paid = rs.getString("paid");
                    due = rs.getString("due");
                    pay_type = rs.getString("payment_type");
                    reg_date = rs.getString("registration_date");

                    row.addElement(id);
                    row.addElement(p_id);
                    row.addElement(artical);
                    row.addElement(category);
                    row.addElement(description);
                    row.addElement(quantity);
                    row.addElement(unit);
                    row.addElement(b_price);
                    row.addElement(s_price);
                    row.addElement(discount);
                    row.addElement(vat);
                    row.addElement(total);
                    row.addElement(paid);
                    row.addElement(due);
                    row.addElement(pay_type);
                    row.addElement(reg_date);
                    data.addElement(row);
                }
                // populated data into table 
                DefaultTableModel tableModel = new DefaultTableModel(data, columnNames);
                jTable_products_details.setModel(tableModel);

            } catch (Exception e) {
                e.printStackTrace();
            } finally {
                try {
                    rs.close();
                    statement1.close();
                    conn.close();
                } catch (SQLException ex) {
                    Logger.getLogger(InventoryForm.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        } else {
            JOptionPane.showMessageDialog(null, "Database Connection Failed");
        }
    }
